class Bitwise2
{
	public static void main(String[] args)
	{
	int a=52;
	int b=-52;
	System.out.println(a|b);
	System.out.println(a&b);
	System.out.println(a^b);
	System.out.println(~a);
	System.out.println(~b);
	System.out.println(a<<2);
	System.out.println(a>>2);
	System.out.println(b<<2);
	System.out.println(b>>2);
	}
}
		


